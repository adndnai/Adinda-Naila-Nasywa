    public void tampilData(String where) {
        Object[] data = {
            "id", "nama","jumlah", "harga"
        };
        tabModel = new DefaultTableModel(null, data);
        databuku.setModel(tabModel);
        
        try {
        st = con.createStatement();
        tabModel.getDataVector().removeAllElements();
        tabModel.fireTableDataChanged();
        


rs = st.executeQuery("SELECT * FROM book " + where);
while (rs.next()) {
Object[] datas = {
rs.getString("id"),
rs.getString("bnama"),
rs.getString("jumlah"),
rs.getString("harga"),
};
tabModel.addRow(datas);
};



    } catch (SQLException e) {
        e.printStackTrace();
    }
        
}

