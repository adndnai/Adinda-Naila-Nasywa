    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
        try {
        st = con.createStatement();
        String id = kodebuku.getText(); // Ambil NIS dari input
        String bnama = namabuku.getText(); // Ambil Nama dari input
        String jumlah = jstok.getText();
        String harga = jharga.getText(); // Ambil Alamat dari input
        
        // Gunakan perintah UPDATE dengan kondisi WHERE sesuai ID (NIS)
        st.executeUpdate("UPDATE book SET "
            + "bnama='" + bnama + "', "
            + "jumlah='" + jumlah + "', "
            + "harga='" + harga + "' WHERE id='" + id + "'");
        
        tampilData(""); // Tampilkan data yang telah diperbarui
        JOptionPane.showMessageDialog(null, "Update Berhasil");
        reset();
    } catch (Exception e) {
        e.printStackTrace(); }
    }//GEN-LAST:event_beditActionPerformed

